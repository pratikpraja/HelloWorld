package util;

public class Calculation {
	public double add(double a, double b) {
		double ans;
		ans = a+b;
		return ans;
	}
	public double sub(double a, double b) {
		double ans;
		ans = a-b;
		return ans;
	}
	public double mul(double a, double b) {
		double ans;
		ans = a*b;
		return ans;
	}
	public double div(double a, double b) {
		double ans;
		ans = a/b;
		return ans;
	}
}
